/* 5. Declare uma variável do tipo double valorEmDolares. Atribua um valor em dólares 
 * a essa variável. Considere que o valor de 1 dólar é equivalente a 4.94 reais. 
 * Realize a conversão do valor em dólares para reais e imprima o resultado formatado.
 */
public class Main {

	public static void main(String[] args) {
		double valorEmDolares, conversao, valorEmReais = 0;
		
		valorEmDolares = 250;
		conversao = 4.94;
		
		valorEmReais = conversao * valorEmDolares;
		
		String mensagem = String.format("\nConvertendo $ %.2f dólares para reais que estão cotados em R$ %.2f resulta em = %.2f dólares ", valorEmDolares, conversao, valorEmReais);
		
		System.out.println("\nConvertendo $ " + valorEmDolares + " dólares para reais que estão cotados em R$ " + conversao + " resulta em R$ " + valorEmReais);
		System.out.println(mensagem);
	}

}
